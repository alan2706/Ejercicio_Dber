#Dados dos (2) números calcule la suma, resta, multiplicación, división y módulo.
numero1 = int(input("Ingrese el primer numero: "))
numero2 = int(input("Ingrese el segundo numero: "))
suma = numero1 + numero2 
resta = numero1 - numero2 
multiplicacion = numero1 * numero2
division = numero1 // numero2 
modulo = numero1 % numero2
print("La suma de sus dos numeros es: " + str(suma))
print("La resta de sus dos numeros es: " + str(resta))
print("La multiplicacion de sus dos numeros es: " + str(multiplicacion))
print("La division de sus dos numeros es: " + str(division))
print("El modulo entre sus dos numeros es: " + str(modulo))